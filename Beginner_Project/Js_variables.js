// Variables are Containers for Storing Data
// JavaScript Variables can be declared in 4 ways:

// Automatically
// Using var
// Using let
// Using const


// Automatically
a = 20
console.log(a);

// using var :=> The var keyword was used in all JavaScript code from 1995 to 2015.
var a = 5;
var b = 6;
console.log(a + b);


// using let :=> The let and const keywords were added to JavaScript in 2015.
let x = "Hello";
let y = "World";
console.log(x + " " + y);

// using const keyword:=> These are constant values and cannot be changed.

const v = 5;
const w = 6;
const z = v + w;
console.log(z);


